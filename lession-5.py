# Math in python
print(2+3)
print(2*3)
print(2**3) #power of 2^3 =8
print(9%6)#modulus operator gives remainder after division, Example: 9/6=1 (3,3) is 6 and remainder is 3 so output will be 3 to (3,3,3) is 9
print(7//2)#floor division, it will give integer value only
print(round(3.7))#rounding off the number to nearest whole no.
print(abs(-3))# absolute value of a number
