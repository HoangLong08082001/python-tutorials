# Tuple variables
tupleVar = (1, 2.5, "Python", True)
print(tupleVar)

tuple1 = (1,2,3)
tuple2 = ("one","two")
print(tuple1 + tuple2) # Concatenation

x = (1,2,3)*2
print(x) # Repetition


print(2 in x) # Membership
print(len(x)) # Length
print(max(x)) # Maximum value
print(min(x)) # Min value
print(sum(x)) # Sum of values
print(sorted(x)) # Sorted list